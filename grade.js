let grade = "A";

switch(grade){
    case "A":
        console.log("Very Good");
        break;
    case "B":
        console.log("Good");
        break;
    case "C":
        console.log("Average");
        break;
    case "D":
        console.log("Bad");
        break;
    case "E":
        console.log("Not Eligible");
        break;
    default:
        console.log("Invalid entry!!!")

}